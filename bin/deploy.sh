#!/usr/bin/env bash
# Bootstrap and redeploy script for condo on a single Linux host (EC2 etc.).
#
# First run on a fresh box:
#   sudo ACME_EMAIL=you@example.com bash -c \
#     "$(curl -fsSL https://raw.githubusercontent.com/Senseering/condo/main/bin/deploy.sh)"
#
# Subsequent redeploys (repo already cloned to APP_DIR):
#   cd /opt/condo && sudo ./bin/deploy.sh
#
# What it does:
#   1. Installs git + Docker if missing.
#   2. Clones (or `git pull`s) the repo into APP_DIR.
#   3. On first run only, generates .env with random POSTGRES_PASSWORD,
#      COOKIE_SECRET, ADMIN_PASSWORD; prints the admin credentials once.
#   4. Builds the condo image (~30 min cold, ~1 min warm) and brings the
#      stack up. Caddy fronts the stack with Let's Encrypt SSL.
#
# Prerequisites you handle outside this script:
#   - DNS A record for $DOMAIN points to this host's public IP
#     (required before first run so Let's Encrypt's HTTP-01 challenge works).
#   - Inbound 80 + 443 open in the host firewall / EC2 security group.

set -euo pipefail

DOMAIN="${DOMAIN:-condo.datamatters.io}"
APP_DIR="${APP_DIR:-/opt/condo}"
REPO_URL="${REPO_URL:-https://github.com/Senseering/condo}"
BRANCH="${BRANCH:-main}"

if [[ $EUID -ne 0 ]]; then
	echo "ERROR: must run as root (use sudo)" >&2
	exit 1
fi

# --- Install prerequisites ---
if ! command -v git >/dev/null 2>&1; then
	echo "==> Installing git"
	apt-get update -qq
	apt-get install -y -qq git ca-certificates curl openssl
fi

if ! command -v docker >/dev/null 2>&1; then
	echo "==> Installing Docker"
	curl -fsSL https://get.docker.com | sh
	systemctl enable --now docker
fi

# --- Clone or update repo ---
if [[ ! -d "$APP_DIR/.git" ]]; then
	echo "==> Cloning $REPO_URL into $APP_DIR"
	git clone --branch "$BRANCH" "$REPO_URL" "$APP_DIR"
fi
cd "$APP_DIR"
echo "==> Updating $APP_DIR to origin/$BRANCH"
git fetch origin "$BRANCH"
git checkout "$BRANCH"
git pull --ff-only

# --- Generate .env on first run ---
if [[ ! -f .env ]]; then
	if [[ -z "${ACME_EMAIL:-}" ]]; then
		echo "ERROR: first run requires ACME_EMAIL (Let's Encrypt registration email)" >&2
		echo "       e.g. sudo ACME_EMAIL=ops@datamatters.io ./bin/deploy.sh" >&2
		exit 1
	fi

	POSTGRES_PASSWORD=$(openssl rand -hex 24)
	COOKIE_SECRET=$(openssl rand -hex 32)
	ADMIN_PASSWORD=$(openssl rand -hex 12)

	umask 077
	cat > .env <<EOF
# Generated by bin/deploy.sh on $(date -u +%FT%TZ).
# Contains secrets. Keep this file private and back it up before destroying the host.

DOMAIN=$DOMAIN
ACME_EMAIL=$ACME_EMAIL

POSTGRES_PASSWORD=$POSTGRES_PASSWORD
COOKIE_SECRET=$COOKIE_SECRET

# Seeded into the database by the init container on first boot.
ADMIN_EMAIL=admin@$DOMAIN
ADMIN_PASSWORD=$ADMIN_PASSWORD
EOF

	cat <<EOF

==> Generated .env with random secrets.

    Initial admin login:
      URL:      https://$DOMAIN/admin/signin
      Email:    admin@$DOMAIN
      Password: $ADMIN_PASSWORD

    Save the password now — it is not stored anywhere else and is not
    re-printed on subsequent runs. The password is also written into the
    database; rotating it later requires updating the admin user via the
    /admin UI, not editing .env.

EOF
fi

# --- Build & up ---
echo "==> Building condo image (cold: ~30 min, warm cache: ~1 min)"
docker compose build condo

echo "==> Starting stack"
docker compose up -d

echo
echo "==> Done. Useful commands:"
echo "    docker compose ps"
echo "    docker compose logs -f condo"
echo "    docker compose logs -f caddy   # SSL issuance progress"
echo
echo "    Open: https://$DOMAIN/admin/signin"
